    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        // TODO add your handling code here:
        
        try{
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url = gen.conection;
            Connection con = DriverManager.getConnection(url);
            String sql = "update toyshido_producto set Nombre=?, Precio=?, Stock=?, Categoria=?, Loca=? where IDProducto="+ Integer.parseInt(txtIDProd.getText());
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, txtname.getText());
            pst.setString(2, txtprice.getText());
            pst.setString(3, spnstock.getValue().toString());
            pst.setString(4, cmbcate.getSelectedItem().toString());
            pst.setString(5, txtloca.getText());
            pst.executeUpdate();
            con.close();
            
            JOptionPane.showMessageDialog(null, "Actualización correcta sobre:"+ txtname.getText() +" !");
                
            txtname.setText("");
            txtIDProd.setText("");
            txtprice.setText("");
            txtloca.setText("");
            spnstock.setValue(Integer.parseInt("0"));
            cmbcate.setSelectedIndex(0);
            
            DefaultTableModel model = (DefaultTableModel)tblprod.getModel();
            model.setRowCount(0);
            
            name = "";
            price = "";
            loca = "";
            stock = "";
            categ = "";
            
            btnclear.setEnabled(false);
            btnedit.setEnabled(false);
            
            showProducts();
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btneditActionPerformed
 
