    private void tblprodMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblprodMouseClicked
        // TODO add your handling code here:
        int i = tblprod.getSelectedRow();
        TableModel model = tblprod.getModel();
        
        txtname.setText(model.getValueAt(i, 1).toString());
        name = model.getValueAt(i, 1).toString();
        txtIDProd.setText(model.getValueAt(i, 0).toString());
        txtloca.setText(model.getValueAt(i, 5).toString());
        loca = model.getValueAt(i, 5).toString();
        txtprice.setText(model.getValueAt(i, 2).toString());
        price = model.getValueAt(i, 2).toString();
        spnstock.setValue(model.getValueAt(i, 3));
        stock = model.getValueAt(i, 3).toString();
        String cate = model.getValueAt(i, 4).toString();
        categ = cate;
        btnclear.setEnabled(true);
        btnedit.setEnabled(true);
        
        switch (cate){
            case "Terror":
                    cmbcate.setSelectedIndex(0);
                    break;
            case "Thriller":
                    cmbcate.setSelectedIndex(1);
                    break;
            case "Anime":
                    cmbcate.setSelectedIndex(2);
                    break;
            case "Disney":
                    cmbcate.setSelectedIndex(3);
                    break;
            case "Videojuegos":
                    cmbcate.setSelectedIndex(4);
                    break;
        }
    }//GEN-LAST:event_tblprodMouseClicked

