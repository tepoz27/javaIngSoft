    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url = gen.conection;
            Connection con = DriverManager.getConnection(url);
            String sql = "update toyshido_usuario set Usuario=?, Contrasena=?, Rol=? where IDUsuario="+ Integer.parseInt(txtid.getText());
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, txtuser.getText());
            pst.setString(2, txtcontra.getText());
            pst.setString(3, cmbrol.getSelectedItem().toString());
            pst.executeUpdate();
            con.close();
            
            JOptionPane.showMessageDialog(null, "Actualización correcta sobre:"+ txtuser.getText() +" !");
                
            txtid.setText("");
            txtuser.setText("");
            txtcontra.setText("");
            
            
            DefaultTableModel model = (DefaultTableModel)tbluser.getModel();
            model.setRowCount(0);
            
            user = "";
            contra = "";
            rol = "";
            
            btnclear.setEnabled(false);
            btnedit.setEnabled(false);
            
            showUser();
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btneditActionPerformed

