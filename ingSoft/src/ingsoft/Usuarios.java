    public ArrayList<User> userList(){
        ArrayList<User> userList = new ArrayList<>();
        try{
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url = gen.conection;
            Connection con = DriverManager.getConnection(url);
            String sql = "Select * from toyshido_usuario";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            User user;
            while (rs.next()){
                user = new User(rs.getInt("IDUsuario"), rs.getString("Usuario"), rs.getString("Contrasena"), rs.getString("Rol"));
                userList.add(user);
            }
            con.close();
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
        return userList;
    }
    
    public void showUser(){
        ArrayList<User> list = userList();
        DefaultTableModel model = (DefaultTableModel)tbluser.getModel();
        Object[] row = new Object[4];
        
        for (int i = 0;i < list.size();i++){
            row[0] = list.get(i).getID();
            row[1] = list.get(i).getUser();
            row[2] = list.get(i).getContra();
            row[3] = list.get(i).getRol();
            model.addRow(row);
        }
    }
